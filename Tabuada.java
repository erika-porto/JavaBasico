
public class Tabuada {
   public static void main(String[] args) {
//	  IMPRIMINDO TABUADA
	   
	   for (int multiplicador = 0; multiplicador <= 10; multiplicador++) {
		   for(int numero = 0; numero <= 10; numero ++) {
			   System.out.print(multiplicador * numero + " ");
		   }
		   System.out.println();
	   }
}
}
